# include <iostream>
using namespace std;
int main(){
    //Post Increment Operation
    cout<<"Post Increment Operation"<<"\n";
    int a = 10;
    int c =  a++;
    cout<<"A = "<<a<<"\n";
    cout<<"C = "<<c<<"\n";
    //Post Increment Operation
    cout<<"Pre Increment Operation"<<"\n";
    int d = 10;
    int e =  ++d;
    cout<<"D = "<<d<<"\n";
    cout<<"E = "<<e<<"\n";
}