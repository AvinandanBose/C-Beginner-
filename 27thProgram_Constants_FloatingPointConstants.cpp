#include<iostream>
using namespace std;
int main(){
    //Decimal Notation
    float a  = 0.976 - 0.71 / 0.5; 
    double b  =  0.975 - 0.712 / 0.53; 
    cout << a << endl;
    cout << b << endl;

    //Exponential Notation
    float c  = 3.4E4;
    double d  = 3e8;
    cout << c << endl;
    cout << d << endl;

    return 0;


}